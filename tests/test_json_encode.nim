suite "golden JSON fixtures — full_span.json":
  test "all AnyValue kinds encode to correct OTLP-JSON field names":
    # Verify the JSON field names per signal type
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avString, strVal: "x"))).hasKey("stringValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avBool, boolVal: true))).hasKey("boolValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avInt, intVal: 1))).hasKey("intValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avDouble, dblVal: 1.0))).hasKey("doubleValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avBytes, bytesVal: @[0x01'u8]))).hasKey("bytesValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avArray, arrayVal: @[]))).hasKey("arrayValue")
    check parseJson(jsonEncodeAnyValue(AnyValue(kind: avKvList, kvlistVal: @[]))).hasKey("kvlistValue")

  test "fixture file — traceId and spanId are lowercase hex":
    let j = parseJson(readFile("tests/fixtures/json/full_span.json"))
    let tid = j["traceId"].getStr()
    let sid = j["spanId"].getStr()
    check tid.len == 32
    check sid.len == 16
    for c in tid: check c in {'0'..'9', 'a'..'f'}
    for c in sid: check c in {'0'..'9', 'a'..'f'}

  test "fixture file — startTimeUnixNano is a JSON string":
    let j = parseJson(readFile("tests/fixtures/json/full_span.json"))
    check j["startTimeUnixNano"].kind == JString

  test "fixture file — intValue attribute is a quoted string":
    let j = parseJson(readFile("tests/fixtures/json/full_span.json"))
    # http.status_code = 201 is intValue
    let attrs = j["attributes"]
    var found = false
    for attr in attrs:
      if attr["key"].getStr() == "http.status_code":
        check attr["value"]["intValue"].kind == JString
        check attr["value"]["intValue"].getStr() == "201"
        found = true
    check found

  test "fixture file — bytesValue attribute is base64":
    let j = parseJson(readFile("tests/fixtures/json/full_span.json"))
    let attrs = j["attributes"]
    var found = false
    for attr in attrs:
      if attr["key"].getStr() == "request.id":
        check attr["value"]["bytesValue"].getStr() == "AQIDBA=="
        found = true
    check found

  test "fixture file — kind is an integer (OTLP-JSON uses enum int not name)":
    # OTLP-JSON maps enums to their integer value, not the name string.
    let j = parseJson(readFile("tests/fixtures/json/full_span.json"))
    check j["kind"].kind == JInt
    check j["kind"].getInt() == 2  # SpanKind.SERVER

