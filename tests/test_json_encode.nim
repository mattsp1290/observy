# ---------------------------------------------------------------------------
# Golden-file JSON fixture tests
# Verify the Nim JSON encoder produces values that match the reference fixtures
# in tests/fixtures/json/ (generated by tools/gen_fixtures.py)
# ---------------------------------------------------------------------------

proc parseHexByte(s: string; i: int): byte =
  byte(parseHexInt(s[i*2 ..< i*2+2]))

suite "golden JSON fixtures — trace_id_hex.json":
  test "traceId is 32 lowercase hex chars (not base64)":
    # Same TraceId as in trace_id_hex.json
    var tid: TraceId
    let hex = "4bf92f3577b34da6a3ce929d0e0e4736"
    for i in 0 ..< 16: tid[i] = parseHexByte(hex, i)
    let encoded = hexEncodeTraceId(tid)
    check encoded == hex
    check encoded.len == 32
    check encoded.find('=') < 0   # no base64 padding
    for c in encoded: check c in {'0'..'9', 'a'..'f'}

  test "spanId is 16 lowercase hex chars (not base64)":
    var sid: SpanId
    let hex = "00f067aa0ba902b7"
    for i in 0 ..< 8: sid[i] = parseHexByte(hex, i)
    let encoded = hexEncodeSpanId(sid)
    check encoded == hex
    check encoded.len == 16
    check encoded.find('=') < 0
    for c in encoded: check c in {'0'..'9', 'a'..'f'}

  test "fixture file confirms hex encoding":
    let j = parseJson(readFile("tests/fixtures/json/trace_id_hex.json"))
    check j["traceId"].getStr() == "4bf92f3577b34da6a3ce929d0e0e4736"
    check j["traceId"].getStr().len == 32
    check j["spanId"].getStr() == "00f067aa0ba902b7"
    check j["spanId"].getStr().len == 16

